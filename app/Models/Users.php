     * @return array
     */
	public static function editFields(){
		return [ 
			"id",
			"fullname",
			"email",
			"status",
			"city",
			"phone",
			"role" 
		];
	}
	

	/**
     * Audit log events
     * 
     * @var array
     */
	protected $auditEvents = [
		'created', 'updated', 'deleted'
	];
}
